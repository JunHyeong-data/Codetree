# 10개의 문자를 공백으로 구분하여 입력받습니다.
# 입력받은 문자열을 공백을 기준으로 분리하여 리스트에 저장합니다.
chars = input().split()

# 입력받은 문자들을 역순으로 출력합니다.
# 리스트의 reverse() 메서드를 사용하거나, 슬라이싱 [::-1]을 사용할 수 있습니다.
# 여기서는 reverse()를 사용하여 원본 리스트를 역순으로 만든 후 출력합니다.
chars.reverse()

# 역순으로 정렬된 문자들을 공백 없이 한 줄로 출력합니다.
print("".join(chars))