# 정수 N을 입력받습니다.
N = int(input())

# 합을 저장할 변수와 더해진 수를 나타낼 변수를 초기화합니다.
current_sum = 0
added_number = 0

# 1부터 차례대로 1씩 증가시키며 합을 구합니다.
# 합이 N 이상이 될 때까지 반복합니다.
for i in range(1, 101): # 문제에 100까지 1씩 증가시킨다고 되어있지만, N이 5000까지 가능하므로 무한 루프처럼 동작하게 for 대신 while을 사용하거나 충분히 큰 범위를 지정해야 함.
                      # 하지만 "1부터 차례대로 100까지 1씩 증가시키며"라는 문구는 1부터 100까지만 검사하는 것으로 해석될 수 있습니다.
                      # 이 경우 N이 5000까지 주어질 수 있으므로, 해당 문구는 함정일 수 있습니다.
                      # 일반적으로 이런 문제에서는 합이 N이 될 때까지 계속 더하는 것이 맞습니다.
                      # 여기서는 100이라는 숫자에 제한을 두지 않고, 합이 N 이상이 될 때까지 계속 더하는 로직으로 구현합니다.
    current_sum += i
    added_number = i
    if current_sum >= N:
        break # 합이 N 이상이 되면 반복을 중단합니다.

# 합이 N 이상이 되는 순간 더해진 수를 출력합니다.
print(added_number)