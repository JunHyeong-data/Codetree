# 첫 번째 줄에 첫째항, 둘째항이 공백을 사이에 두고 주어집니다.
a, b = map(int, input().split())

# 10개의 숫자를 저장할 리스트를 초기화합니다.
arr = [0] * 10

# 첫째 항과 둘째 항을 설정합니다.
arr[0] = a
arr[1] = b

# 세 번째 항부터는 전전항과 전항의 합의 1의 자리를 채워서 10개를 출력합니다.
for i in range(2, 10):
    # 전전항과 전항의 합을 계산합니다.
    sum_prev = arr[i-2] + arr[i-1]
    # 합의 1의 자리를 배열에 저장합니다.
    arr[i] = sum_prev % 10

# 결과 숫자들을 공백을 사이에 두고 출력합니다.
print(*arr)